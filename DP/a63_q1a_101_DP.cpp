#include<bits/stdc++.h>
int n,dp[10001][2][2];
main(){
  std::cin>>n;
  dp[2][0][0]=1;
  dp[2][0][1]=1;
  dp[2][1][0]=1;
  dp[2][1][1]=1;
  for(int i=3;i<=n;++i){
    dp[i][0][0]=(dp[i-1][0][0]+dp[i-1][1][0])%100000007 ;
    dp[i][0][1]=dp[i-1][0][0]%100000007 ;
    dp[i][1][0]=(dp[i-1][0][1]+dp[i-1][1][1])%100000007 ;
    dp[i][1][1]=(dp[i-1][1][1]+dp[i-1][0][1])%100000007 ;
  }
  std::cout<<(dp[n][0][0]+dp[n][0][1]+dp[n][1][0]+dp[n][1][1])%100000007 ;
}
